import { createClient } from '@supabase/supabase-js';
import nodemailer from 'nodemailer';

const SUPABASE_URL = process.env.SUPABASE_URL;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_KEY;
const RESEND_API_KEY = process.env.RESEND_API_KEY;
const EMAIL_FROM = process.env.EMAIL_FROM || 'BrightKey Solutions <noreply@brightkeysolutions.com>';

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method Not Allowed' });
  }

  const { email } = req.body;
  if (!email) {
    return res.status(400).json({ error: 'Email is required.' });
  }

  if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
    return res.status(500).json({ error: 'Supabase configuration missing on server.' });
  }

  const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, {
    auth: { persistSession: false }
  });

  try {
    // 1. Resolve full name and company_id
    const { data: emp } = await supabase
      .from('employees')
      .select('first_name, last_name, company_id')
      .eq('email', email.toLowerCase().trim())
      .limit(1)
      .maybeSingle();

    const fullName = emp ? `${emp.first_name} ${emp.last_name}` : email.split('@')[0];

    // 2. Generate Supabase recovery link
    let origin = 'https://brightkeysolutions.com';
    const referer = req.headers.referer || '';
    if (referer.includes('localhost') || referer.includes('127.0.0.1')) {
      origin = 'http://localhost:3000';
    }
    const redirectTo = `${origin}/admin`;

    const { data: linkData, error: linkError } = await supabase.auth.admin.generateLink({
      type: 'recovery',
      email: email.toLowerCase().trim(),
      options: { redirectTo }
    });

    if (linkError) {
      return res.status(400).json({ error: `Failed to generate recovery link: ${linkError.message}` });
    }

    const resetLink = linkData?.properties?.action_link;
    if (!resetLink) {
      return res.status(500).json({ error: 'Action link not generated by Supabase Auth.' });
    }

    // 3. Fetch company-specific Resend or SMTP credentials if they exist
    let integration = null;
    if (emp && emp.company_id) {
      const { data: integrationData } = await supabase
        .from('company_integrations')
        .select('hr_resend_api_key, hr_resend_from_email, hr_smtp_host, hr_smtp_port, hr_smtp_user, hr_smtp_pass, resend_api_key, resend_from_email, smtp_host, smtp_port, smtp_user, smtp_pass')
        .eq('company_id', emp.company_id)
        .maybeSingle();
      integration = integrationData;
    }

    const activeResendApiKey = integration?.hr_resend_api_key || integration?.resend_api_key || RESEND_API_KEY;
    const activeEmailFrom = integration?.hr_resend_from_email || integration?.resend_from_email || EMAIL_FROM;
    const smtpUser = integration?.hr_smtp_user || integration?.smtp_user;
    const smtpPass = integration?.hr_smtp_pass || integration?.smtp_pass;
    const smtpHost = integration?.hr_smtp_host || integration?.smtp_host || 'smtp.gmail.com';
    const smtpPort = integration?.hr_smtp_port || integration?.smtp_port || 465;

    let emailSent = false;

    if (smtpUser && smtpPass) {
      // Send via SMTP (Gmail)
      try {
        const transporter = nodemailer.createTransport({
          host: smtpHost,
          port: parseInt(smtpPort) || 465,
          secure: (parseInt(smtpPort) || 465) === 465,
          auth: {
            user: smtpUser,
            pass: smtpPass
          }
        });

        await transporter.sendMail({
          from: `"BrightKey Solutions" <${smtpUser}>`,
          to: email.toLowerCase().trim(),
          subject: 'Reset Your BrightKey Password',
          html: `
            <div style="font-family: sans-serif; padding: 24px; color: #374151; max-width: 600px; margin: 0 auto; border: 1px solid #e5e7eb; border-radius: 8px; background-color: #ffffff;">
              <h2 style="color: #0891b2; font-weight: bold; margin-bottom: 20px; text-align: center;">Reset Your BrightKey Password</h2>
              <p>Hello ${fullName},</p>
              <p>We received a request to reset the password for your BrightKey Solutions account. Please click the button below to choose a new password. This secure link is valid for 24 hours.</p>
              <p style="margin-top: 24px; text-align: center;">
                <a href="${resetLink}" style="background-color: #06b6d4; color: white; padding: 12px 24px; text-decoration: none; border-radius: 6px; font-weight: bold; display: inline-block;">
                  Reset Password
                </a>
              </p>
              <p style="font-size: 13px; color: #9ca3af; margin-top: 32px; border-top: 1px solid #e5e7eb; padding-top: 16px;">
                If you didn't request a password reset, please ignore this email or contact your workspace administrator if you have concerns.
              </p>
            </div>
          `
        });

        emailSent = true;
      } catch (err) {
        console.error('Failed to dispatch reset email via SMTP:', err);
      }
    } else {
      // Fallback to Resend (either tenant's own or system-wide)
      if (activeResendApiKey) {
        try {
          const mailRes = await fetch('https://api.resend.com/emails', {
            method: 'POST',
            headers: {
              'Authorization': `Bearer ${activeResendApiKey}`,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify({
              from: activeEmailFrom,
              to: email.toLowerCase().trim(),
              subject: 'Reset Your BrightKey Password',
              html: `
                <div style="font-family: sans-serif; padding: 24px; color: #374151; max-width: 600px; margin: 0 auto; border: 1px solid #e5e7eb; border-radius: 8px; background-color: #ffffff;">
                  <h2 style="color: #0891b2; font-weight: bold; margin-bottom: 20px; text-align: center;">Reset Your BrightKey Password</h2>
                  <p>Hello ${fullName},</p>
                  <p>We received a request to reset the password for your BrightKey Solutions account. Please click the button below to choose a new password. This secure link is valid for 24 hours.</p>
                  <p style="margin-top: 24px; text-align: center;">
                    <a href="${resetLink}" style="background-color: #06b6d4; color: white; padding: 12px 24px; text-decoration: none; border-radius: 6px; font-weight: bold; display: inline-block;">
                      Reset Password
                    </a>
                  </p>
                  <p style="font-size: 13px; color: #9ca3af; margin-top: 32px; border-top: 1px solid #e5e7eb; padding-top: 16px;">
                    If you didn't request a password reset, please ignore this email or contact your workspace administrator if you have concerns.
                  </p>
                </div>
              `
            })
          });

          if (mailRes.ok) {
            emailSent = true;
          } else {
            const mailErr = await mailRes.json();
            console.error('Resend API error:', mailErr);
          }
        } catch (err) {
          console.error('Failed to dispatch reset email via Resend:', err);
        }
      } else {
        console.warn('Resend API key not defined. Reset email dispatch skipped.');
      }
    }

    return res.status(200).json({ success: true, email_sent: emailSent });

  } catch (err) {
    console.error('Reset password handler crash:', err);
    return res.status(500).json({ error: 'Internal Server Error' });
  }
}
