import { createClient } from '@supabase/supabase-js';

const SUPABASE_URL = process.env.SUPABASE_URL;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_KEY;
const RESEND_API_KEY = process.env.RESEND_API_KEY;
const EMAIL_FROM = process.env.EMAIL_FROM || 'BrightKey Solutions <noreply@brightkeysolutions.com>';

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method Not Allowed' });
  }

  const { email } = req.body;
  if (!email) {
    return res.status(400).json({ error: 'Email is required.' });
  }

  if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
    return res.status(500).json({ error: 'Supabase configuration missing on server.' });
  }

  const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, {
    auth: { persistSession: false }
  });

  try {
    // 1. Resolve full name
    const { data: emp } = await supabase
      .from('employees')
      .select('first_name, last_name')
      .eq('email', email.toLowerCase().trim())
      .limit(1)
      .maybeSingle();

    const fullName = emp ? `${emp.first_name} ${emp.last_name}` : email.split('@')[0];

    // 2. Generate Supabase recovery link
    const origin = req.headers.referer ? new URL(req.headers.referer).origin : 'https://www.brightkeysolutions.com';
    const redirectTo = `${origin}/login.html`;

    const { data: linkData, error: linkError } = await supabase.auth.admin.generateLink({
      type: 'recovery',
      email: email.toLowerCase().trim(),
      options: { redirectTo }
    });

    if (linkError) {
      return res.status(400).json({ error: `Failed to generate recovery link: ${linkError.message}` });
    }

    const resetLink = linkData?.properties?.action_link;
    if (!resetLink) {
      return res.status(500).json({ error: 'Action link not generated by Supabase Auth.' });
    }

    // 3. Dispatch Email via Resend
    let emailSent = false;
    if (RESEND_API_KEY) {
      try {
        const mailRes = await fetch('https://api.resend.com/emails', {
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${RESEND_API_KEY}`,
            'Content-Type': 'application/json'
          },
          body: JSON.stringify({
            from: EMAIL_FROM,
            to: email.toLowerCase().trim(),
            subject: 'Reset Your BrightKey Password',
            html: `
              <div style="font-family: sans-serif; padding: 24px; color: #374151; max-width: 600px; margin: 0 auto; border: 1px solid #e5e7eb; border-radius: 8px; background-color: #ffffff;">
                <h2 style="color: #0891b2; font-weight: bold; margin-bottom: 20px; text-align: center;">Reset Your BrightKey Password</h2>
                <p>Hello ${fullName},</p>
                <p>We received a request to reset the password for your BrightKey Solutions account. Please click the button below to choose a new password. This secure link is valid for 24 hours.</p>
                <p style="margin-top: 24px; text-align: center;">
                  <a href="${resetLink}" style="background-color: #06b6d4; color: white; padding: 12px 24px; text-decoration: none; border-radius: 6px; font-weight: bold; display: inline-block;">
                    Reset Password
                  </a>
                </p>
                <p style="font-size: 13px; color: #9ca3af; margin-top: 32px; border-top: 1px solid #e5e7eb; padding-top: 16px;">
                  If you didn't request a password reset, please ignore this email or contact your workspace administrator if you have concerns.
                </p>
              </div>
            `
          })
        });

        if (mailRes.ok) {
          emailSent = true;
        } else {
          const mailErr = await mailRes.json();
          console.error('Resend API error:', mailErr);
        }
      } catch (err) {
        console.error('Failed to dispatch reset email via Resend:', err);
      }
    } else {
      console.warn('RESEND_API_KEY not defined. Reset email dispatch skipped.');
    }

    return res.status(200).json({ success: true, email_sent: emailSent });

  } catch (err) {
    console.error('Reset password handler crash:', err);
    return res.status(500).json({ error: 'Internal Server Error' });
  }
}
